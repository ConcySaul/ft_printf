#include "../includes/libft.h"

int	ft_numlen(long n)
{
	int	i;

	i = 0;
	if (n <= 0)
	{
		i += 1;
		n *= -1;
	}
	while (n > 0)
	{
		i++;
		n /= 10;
	}
	return (i);
}
